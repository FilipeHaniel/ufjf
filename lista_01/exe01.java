class Exe01 {
    public static void main(String[] args) {

        double theSum = sum(2, 3);

        System.out.println("The sum of two numbers is: " + theSum);
    }

    static double sum(double numA, double numB) {
        double sumOfTwoNumbers = numA + numB;

        return sumOfTwoNumbers;
    }
}
